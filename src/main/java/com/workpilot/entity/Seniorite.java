package com.workpilot.entity;

// Enumération représentant la séniorité d'une ressource
public enum Seniorite {
    JUNIOR,  // Ressource ayant entre 0 et 2 ans d'expérience (120 DT)
    INTERMEDIAIRE,  // Ressource ayant entre 2 et 6 ans d'expérience (150 DT)
    Senior, // Ressource ayant entre 6 et 12 ans d'expérience (250 DT)
    SeniorManager // Ressource ayant plus de 12 ans d'expérience (280 DT)
}
